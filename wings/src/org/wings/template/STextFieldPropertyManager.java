package org.wings.template;

import org.wings.STextField;

/**
 * TODO: documentation
 *
 * @author <a href="mailto:haaf@mercatis.de">Armin Haaf</a>
 * @version $Revision$
 */
public class STextFieldPropertyManager
    extends SComponentPropertyManager
{
    static final Class[] classes = {STextField.class};

    public STextFieldPropertyManager() {
    }

    public void setProperty(Object o, String name, String value) {
        STextField c = (STextField)o;
        if ( name.equals("COLS") )
            c.setColumns(Integer.parseInt(value));
        else
            super.setProperty(o, name, value);
    }

    public Class[] getSupportedClasses() {
        return classes;
    }
}

